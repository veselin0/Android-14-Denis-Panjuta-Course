package com.example.kotlinbasics

fun main() {

    var myAge: UShort = 35u
    println(myAge)

    myAge = 36u
    println(myAge)

    val pi = 3.14f
    println(pi)

    val myTrue: Boolean = true
    val myFalse: Boolean = false

    println(myTrue || myFalse)
    println(myTrue && myFalse)
    println(!myTrue)
}